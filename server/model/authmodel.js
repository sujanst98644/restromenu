import mongoose from "mongoose";

const authSchema = new mongoose.Schema({
    Username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        default: "user",
    },

})
export const Auth = mongoose.model("User", authSchema);